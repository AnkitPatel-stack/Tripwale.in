  // Utility function to filter packages
  export const filterPackages = (filters) => {
    let filtered = [...allPackages]
  
    if (filters.priceRange) {
      filtered = filtered.filter(pkg => 
        pkg.price >= filters.priceRange[0] && pkg.price <= filters.priceRange[1]
      )
    }
  
    if (filters.destination) {
      filtered = filtered.filter(pkg =>
        pkg.destinations.some(dest =>
          dest.toLowerCase().includes(filters.destination.toLowerCase())
        )
      )
    }
  
    if (filters.duration) {
      filtered = filtered.filter(pkg => pkg.duration === filters.duration)
    }
  
    if (filters.theme) {
      // This would require adding theme field to packages
      // filtered = filtered.filter(pkg => pkg.themes.includes(filters.theme))
    }
  
    return filtered
  }
  
